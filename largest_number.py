def find_the_largest(number1, number2, number3):
	largest = 0
	if number1 > largest:
		largest = number1
	if number2 > largest :
		largest = number2
	if number3 > largest :
		largest = number3
	return largest



def find_the_minimum(number1, number2, number3):
	
	minimum = number1

	
	if number2 < minimum :
		minimum = number2
	if number3 < minimum :
		minimum = number3

	return minimum
