# (Arithmetic) For each of the arithmetic operators +, -, *, /, // and **, display the value of an expression with # # 27.5 as the left operand and 2 as the right operand.

digit1 = 27.5
digit2 = 2

print(digit1 + digit2)
print(digit1 - digit2)
print(digit1 * digit2)
print(digit1 / digit2)
print(digit1 // digit2)
print(digit1 ** digit2)